package com.model.owner;

public class OwnerDTO {
	private int owner_no;
	private String owner_id;
	private String owner_pwd;
	private String owner_name;
	private String owner_phone;
	private String owner_email;
	private String owner_business;
	public int getOwner_no() {
		return owner_no;
	}
	public void setOwner_no(int owner_no) {
		this.owner_no = owner_no;
	}
	public String getOwner_id() {
		return owner_id;
	}
	public void setOwner_id(String owner_id) {
		this.owner_id = owner_id;
	}
	public String getOwner_pwd() {
		return owner_pwd;
	}
	public void setOwner_pwd(String owner_pwd) {
		this.owner_pwd = owner_pwd;
	}
	public String getOwner_name() {
		return owner_name;
	}
	public void setOwner_name(String owner_name) {
		this.owner_name = owner_name;
	}
	public String getOwner_phone() {
		return owner_phone;
	}
	public void setOwner_phone(String owner_phone) {
		this.owner_phone = owner_phone;
	}
	public String getOwner_email() {
		return owner_email;
	}
	public void setOwner_email(String owner_email) {
		this.owner_email = owner_email;
	}
	public String getOwner_business() {
		return owner_business;
	}
	public void setOwner_business(String owner_business) {
		this.owner_business = owner_business;
	}
}