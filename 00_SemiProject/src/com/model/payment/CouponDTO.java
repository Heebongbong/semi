package com.model.payment;

public class CouponDTO {
	private int coup_no;
	private int coup_promno;
	private String coup_name;
	private String coup_info;
	private int coup_sale;
	private String coup_serialno;
	public int getCoup_no() {
		return coup_no;
	}
	public void setCoup_no(int coup_no) {
		this.coup_no = coup_no;
	}
	public int getCoup_promno() {
		return coup_promno;
	}
	public void setCoup_promno(int coup_promno) {
		this.coup_promno = coup_promno;
	}
	public String getCoup_name() {
		return coup_name;
	}
	public void setCoup_name(String coup_name) {
		this.coup_name = coup_name;
	}
	public String getCoup_info() {
		return coup_info;
	}
	public void setCoup_info(String coup_info) {
		this.coup_info = coup_info;
	}
	public int getCoup_sale() {
		return coup_sale;
	}
	public void setCoup_sale(int coup_sale) {
		this.coup_sale = coup_sale;
	}
	public String getCoup_serialno() {
		return coup_serialno;
	}
	public void setCoup_serialno(String coup_serialno) {
		this.coup_serialno = coup_serialno;
	}
}
