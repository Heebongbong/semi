package com.model.promotion;

public class PromotionDTO {
	private int prom_no;
	private String prom_name;
	private String prom_info;
	private String prom_folder;
	public int getProm_no() {
		return prom_no;
	}
	public void setProm_no(int prom_no) {
		this.prom_no = prom_no;
	}
	public String getProm_name() {
		return prom_name;
	}
	public void setProm_name(String prom_name) {
		this.prom_name = prom_name;
	}
	public String getProm_info() {
		return prom_info;
	}
	public void setProm_info(String prom_info) {
		this.prom_info = prom_info;
	}
	public String getProm_folder() {
		return prom_folder;
	}
	public void setProm_folder(String prom_folder) {
		this.prom_folder = prom_folder;
	}
}
